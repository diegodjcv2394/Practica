    public String validar(String num) {
        String res="";
        try {
            if (Integer.valueOf(num) < 1 || Integer.valueOf(num) > 31) {
                String[] s = {"" + (mod.getRowCount() + 1), jTextField1.getText(), "ERROR"};
                res="ERROR";
                mod.addRow(s);
            } else {
                if (Integer.valueOf(num) < 11) {
                    String[] s = {"" + (mod.getRowCount() + 1), jTextField1.getText(), "0% RECARGO"};
                    res="0% RECARGO";
                    mod.addRow(s);
                } else {
                    if (Integer.valueOf(num) < 21) {
                        String[] s = {"" + (mod.getRowCount() + 1), jTextField1.getText(), "2% RECARGO"};
                        res="2% RECARGO";
                        mod.addRow(s);
                    } else {
                        String[] s = {"" + (mod.getRowCount() + 1), jTextField1.getText(), "4% RECARGO"};
                        res="4% RECARGO";
                        mod.addRow(s);
                    }
                }
            }
        } catch (NumberFormatException e) {
            if (jTextField1.getText().equals("")) {
                String[] s = {"" + (mod.getRowCount() + 1), "NULO", "ERROR"};
                res="ERROR";
                mod.addRow(s);
            } else {
                String[] s = {"" + (mod.getRowCount() + 1), jTextField1.getText(), "ERROR"};
                res="ERROR";
                mod.addRow(s);
            }
        }
        return res;
    }
    
    
